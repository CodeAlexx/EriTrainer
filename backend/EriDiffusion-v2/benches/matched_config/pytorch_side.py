#!/usr/bin/env python3
"""PyTorch side of the matched-config benchmark.

Loads inputs.safetensors generated by gen_inputs.py, runs each op
WARMUP iterations then ITERS timed iterations using torch.cuda.Event,
records median + p90 in microseconds. Writes pt_results.json.

All ops run with torch.no_grad() and torch.inference_mode() — this is
forward-only kernel timing, not training. PyTorch's caching allocator is
left at its default (that IS its production configuration).
"""
from __future__ import annotations

import json
import math
import os
import sys
import time
from pathlib import Path
from statistics import median

import torch
import torch.nn.functional as F
from safetensors.torch import load_file

HERE = Path(__file__).parent
INPUTS = HERE / "inputs.safetensors"
OUT = HERE / "pt_results.json"

WARMUP = 20
ITERS = 100
DEVICE = torch.device("cuda:0")


def time_op(fn, label):
    """Time `fn` (which takes no args, returns a tensor we discard).

    `fn` must construct any temporary index tensors itself if needed; we
    only measure the kernel launch / execution. We re-record CUDA events
    per iteration and synchronize once at the end of each iteration.
    """
    # Warmup
    for _ in range(WARMUP):
        out = fn()
    torch.cuda.synchronize()

    times_us = []
    start = torch.cuda.Event(enable_timing=True)
    end = torch.cuda.Event(enable_timing=True)
    for _ in range(ITERS):
        torch.cuda.synchronize()
        start.record()
        out = fn()
        end.record()
        end.synchronize()
        times_us.append(start.elapsed_time(end) * 1000.0)  # ms → µs
    del out
    times_us.sort()
    p90 = times_us[int(0.9 * (ITERS - 1))]
    med = median(times_us)
    return med, p90


def percentile_sorted(sorted_list, p):
    if not sorted_list:
        return float("nan")
    k = int(p * (len(sorted_list) - 1))
    return sorted_list[k]


def main():
    if not INPUTS.exists():
        print(f"ERROR: {INPUTS} missing. Run gen_inputs.py first.", file=sys.stderr)
        sys.exit(1)

    print(f"Loading {INPUTS} on {DEVICE}...")
    tensors = load_file(str(INPUTS), device="cuda:0")
    # Force allocation + first-touch
    for k, v in tensors.items():
        tensors[k] = v.contiguous()
    torch.cuda.synchronize()

    results = []

    def record(name, shape, dtype, fn):
        med_us, p90_us = time_op(fn, name)
        results.append({
            "name": name,
            "shape": list(shape),
            "dtype": dtype,
            "median_us": med_us,
            "p90_us": p90_us,
            "iters": ITERS,
        })
        print(f"  {name:35s} {str(shape):30s} median={med_us:8.2f}us  p90={p90_us:8.2f}us")

    with torch.inference_mode():
        # ── Tier 1: elementwise ────────────────────────────────────────────
        print("\n[Tier 1] Elementwise")
        for label in ("small", "hot", "large"):
            x = tensors[f"x_bf16_{label}"]
            y = tensors[f"y_bf16_{label}"]
            record(f"silu/{label}", x.shape, "bf16", lambda x=x: F.silu(x))
            record(f"gelu/{label}", x.shape, "bf16", lambda x=x: F.gelu(x))
            record(f"add/{label}",  x.shape, "bf16", lambda x=x, y=y: x + y)
            record(f"mul/{label}",  x.shape, "bf16", lambda x=x, y=y: x * y)
            record(f"mul_scalar/{label}", x.shape, "bf16", lambda x=x: x * 0.5)

        # ── Tier 2: reductions ─────────────────────────────────────────────
        print("\n[Tier 2] Reductions")
        for label in ("small", "hot", "large"):
            x = tensors[f"x_bf16_{label}"]
            record(f"sum_full/{label}",   x.shape, "bf16", lambda x=x: x.sum())
            record(f"sum_dim_last/{label}", x.shape, "bf16",
                   lambda x=x: x.sum(dim=-1, keepdim=True))
            record(f"mean_full/{label}",  x.shape, "bf16", lambda x=x: x.mean())

        # ── Tier 3: norms ──────────────────────────────────────────────────
        print("\n[Tier 3] Norms")
        for label, last_dim in (("zimage", 2560), ("klein", 4096)):
            x = tensors[f"norm_x_{label}"]
            w = tensors[f"norm_w_{label}"]
            b = tensors[f"norm_b_{label}"]
            # PyTorch RMSNorm functional (available 2.4+)
            if hasattr(F, "rms_norm"):
                record(f"rms_norm/{label}", x.shape, "bf16",
                       lambda x=x, w=w, ld=last_dim: F.rms_norm(x, (ld,), w, eps=1e-6))
            else:
                # Fallback: manual implementation
                def _rms(x=x, w=w, ld=last_dim):
                    var = x.to(torch.float32).pow(2).mean(dim=-1, keepdim=True)
                    out = x.to(torch.float32) * torch.rsqrt(var + 1e-6)
                    return (out * w.to(torch.float32)).to(torch.bfloat16)
                record(f"rms_norm/{label}", x.shape, "bf16", _rms)
            record(f"layer_norm/{label}", x.shape, "bf16",
                   lambda x=x, w=w, b=b, ld=last_dim: F.layer_norm(x, (ld,), w, b, eps=1e-5))

        # ── Tier 4: GEMM ───────────────────────────────────────────────────
        print("\n[Tier 4] GEMM")
        a = tensors["gemm_sq_a"]
        b_sq = tensors["gemm_sq_b"]
        record("matmul/sq_4096", a.shape, "bf16", lambda a=a, b=b_sq: a @ b)

        x = tensors["gemm_rect_x"]
        # Linear stores weight as [out, in]; we matmul as x @ w.T or use F.linear directly
        w_oi = tensors["gemm_rect_w_oi"]  # [out=2560, in=2560]
        bias = tensors["gemm_rect_bias"]
        record("matmul/rect_4096x2560",
               x.shape, "bf16",
               lambda x=x, w=w_oi: F.linear(x, w))  # no bias
        record("linear_bias/rect_4096x2560",
               x.shape, "bf16",
               lambda x=x, w=w_oi, b=bias: F.linear(x, w, b))

        # ── Tier 5: attention ──────────────────────────────────────────────
        print("\n[Tier 5] SDPA")
        q = tensors["attn_q"]
        k = tensors["attn_k"]
        v = tensors["attn_v"]
        record("sdpa/qkv_4096_h24_d128", q.shape, "bf16",
               lambda q=q, k=k, v=v: F.scaled_dot_product_attention(q, k, v))

        # ── Tier 6: layout ─────────────────────────────────────────────────
        print("\n[Tier 6] Layout (materialised)")
        l3 = tensors["layout_r3"]   # [1, 4096, 2560]
        l4 = tensors["layout_r4"]   # [1, 24, 4096, 128]
        # Material transpose last-2 dims, then contiguous
        record("transpose_contig/r3", l3.shape, "bf16",
               lambda x=l3: x.transpose(-1, -2).contiguous())
        # permute([2,0,1,3]) + reshape — match flame's permute_generic case.
        # On [1, 24, 4096, 128]: permute(2,0,1,3) → [4096, 1, 24, 128], then
        # reshape to [4096, 24*128] requires materialised contiguous.
        record("permute_reshape/r4", l4.shape, "bf16",
               lambda x=l4: x.permute(2, 0, 1, 3).contiguous().reshape(4096, 24 * 128))

        # ── Tier 7: cast ───────────────────────────────────────────────────
        print("\n[Tier 7] Cast")
        for label in ("small", "hot", "large"):
            x_bf = tensors[f"x_bf16_{label}"]
            x_f32 = tensors[f"x_f32_{label}"]
            record(f"cast_bf16_to_f32/{label}", x_bf.shape, "bf16->f32",
                   lambda x=x_bf: x.to(torch.float32))
            record(f"cast_f32_to_bf16/{label}", x_f32.shape, "f32->bf16",
                   lambda x=x_f32: x.to(torch.bfloat16))

    out_json = {
        "torch_version": torch.__version__,
        "device": torch.cuda.get_device_name(0),
        "iters": ITERS,
        "warmup": WARMUP,
        "ops": results,
    }
    OUT.write_text(json.dumps(out_json, indent=2))
    print(f"\nWrote {OUT}  ({len(results)} ops)")


if __name__ == "__main__":
    main()
